public class Team649Utils {

    public static final int sign(double x) {
        return x < 0 ? -1 : 1;
    }

    public static final int sign(float x) {
        return x < 0 ? -1 : 1;
    }

    public static final int sign(int x) {
        return x < 0 ? -1 : 1;
    }
}
